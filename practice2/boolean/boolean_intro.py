# bool() examples
print(bool("Hello"))   # True 
print(bool(""))        # False
print(bool(123))       # True 
print(bool(0))         # False
print(bool([]))        # False
print(bool([1, 2]))    # True 
print(bool(None))      # False

# empty / zero / None -> False, most others -> True