a = 10
b = 7

print(a > b)     # True
print(a < b)     # False
print(a == b)    # False
print(a != b)    # True
print(a >= 10)   # True
print(b <= 6)    # False

# Strings compare lexicographically
print("apple" < "banana")  # True

print(5 == 5.0)     # True (int and float can be equal by value)
print(5 == "5")     # False (string vs int)